                <div class="faq01">
                    <h3 class="h3Title">미국시장</h3>
                    <div class="faq">
                        <ul>
                            <li class="article">
                                <p class="q"><a href="#a1" class="trigger">미국 주식시장에도 우리나라의 코스피나 코스닥처럼 여러 거래소가 있나요?</a>
                                </p>
                                <div class="a">
                                    미국 주식시장 중 투자자들이 주로 투자하는 곳은 뉴욕증권거래소(NYSE), 나스닥(NASDAQ), 아멕스(AMEX) 세 곳입니다.
                                    <br><br>
                                    뉴욕증권거래소는 미국 시장 최대 규모의 거래소로, 3,100여 개의 종목이 상장되어 있습니다. 엄격한 상장조건 요구로 양질의 기업들이
                                    상장됩니다.
                                    <br><br>
                                    나스닥은 미국 내 2위 규모의 전자거래소입니다. 주로 하이테크 및 인터넷 기업들의 거래가 이루어지며 3,800여 개의 종목이
                                    상장되어 있습니다.
                                    <br><br>
                                    아멕스는 초기 ETF와 옵션거래에서 두각을 보였으나, 현재는 중소형 성장주에 특화된 시장입니다.
                                </div>
                            </li>
                            <li class="article">
                                <p class="q"><a href="#a2" class="trigger">미국 주식을 대표하는 지수는 무엇인가요?</a></p>
                                <div class="a">
                                    다우존스 산업평균지수, 나스닥 종합주가지수, S&P500지수 등이 있습니다.<br>
                                    다우산업지수는 뉴욕증시에 상장된 우량기업 30개를 대표종목으로 산출하는 세계적인 주가지수입니다.
                                    <br><br>
                                    나스닥종합지수는 나스닥에 등록돼있는 3000개가량의 보통주를 가중평균하여 나타낸 지수입니다.
                                    <br><br>
                                    S&P500지수는 스탠다드앤푸어스(Standard&Poor’s)가 집계하는 지수입니다. 뉴욕증권거래소에 상장된 대형 기업 500개의
                                    주식으로 구성되어 있으며 시가총액 방식을 사용합니다.
                                </div>
                            </li>
                            <li class="article">
                                <p class="q"><a href="#a3" class="trigger">미국 주식과 우리나라 주식시장의 다른점은?</a></p>
                                <div class="a">
                                    <ul>
                                        <li><b>1.</b>
                                            <span class="p">우리나라 주식시장에서 사용하는 ‘종목코드'대신 심볼(Symbol, Ticker)을 사용합니다.
                                                뉴욕증권거래소는 1~3개의 영문을,
                                                나스닥은 4~5개의 영문을 사용하고 있습니다.</span>
                                            <span class="info">ex) 애플(Apple)의 티커는 AAPL, 마이크로소프트(Microsoft)의 티커는 MSFT,
                                                월마트(Walmart)의 티커는 WMT</span>
                                        </li>
                                        <li><b>2.</b>
                                            <span class="p">실시간 시세가 무료가 아니며 투자자별(개인/외국인/기관) 실시간 수급 데이터를 제공하지
                                                않습니다.</span>
                                        </li>
                                        <li><b>3.</b>
                                            <span class="p">프리마켓(정규장 시작 전 5시간 30분간 진행)과 애프터마켓(정규장 마감 후 4시간 진행)이 존재하며, 미국
                                                상장사의 기업
                                                실적은 특별한 사유가 없는 한 프리마켓과 애프터마켓 시간에 발표됩니다.</span>
                                        </li>
                                        <li><b>4.</b>
                                            <span class="p">상-하한가제도가 없고 액면가 개념이 없으며 개인의 공매도가 허용됩니다.</span>
                                        </li>
                                    </ul>
                                </div>
                            </li>
                        </ul>
                    </div>
